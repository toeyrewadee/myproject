<b>- Facebook ปรับอัลกอริทึม News Feed ลดความสำคัญข่าวปลอม, ให้ความสำคัญกระแสเรียลไทม์</b>
ช่วงนี้ Facebook ประกาศปรับอัลกอริทึม News Feed ค่อนข้างบ่อย รอบนี้ปรับ 2 เรื่องคือเรื่องข่าวปลอม และการใช้ข้อมูลเรียลไทม์มาพิจารณามากขึ้น
ประเด็นแรกในเรื่องข่าวปลอม Facebook เพิ่มปัจจัยตัวใหม่ที่ช่วยวิเคราะห์ว่าโพสต์นั้นเป็นข่าวปลอมหรือสแปม รูปแบบการทำงานคือ Facebook ศึกษาโพสต์ข่าวปลอมจากเพจคุณภาพต่ำ
แล้วสร้างโมเดลขึ้นมาเทรนให้แยกแยะโพสต์ลักษณะเดียวกันจากเพจอื่นๆ (เช่น ถ้าโพสต์ถูกกด hide จากผู้ใช้บ่อย ก็มีแนวโน้มสูงว่าเป็นโพสต์ข่าวปลอม) ในทางกลับกัน ถ้าโพสต์ของเรา
มีแนวโน้มว่าจะเป็นของจริง ก็มีโอกาสถูกแสดงผลเพิ่มขึ้น ประเด็นต่อมาคือการใช้ข้อมูลแบบเรียลไทม์ ช่วยคัดเลือกการแสดงเนื้อหาที่เกี่ยวข้องกับช่วงเวลานั้น เช่น ถ้าทีมฟุตบอลที่เราเชียร์ชนะ
การแข่งขัน ในช่วงนั้น Facebook อาจแสดงผลเนื้อหาที่เกี่ยวกับฟุตบอลนัดนั้นมากขึ้น (พอหมดระยะก็กลับไปเข้าอัลกอริทึมปกติ) เรื่องนี้ Facebook จะพิจารณาจากผู้ใช้คนอื่นๆ ว่าพูดถึงเรื่องนี้แค่ไหน
หรือมีโพสต์ที่ถูกคนอื่นๆ ให้ความสำคัญเป็นจำนวนมาก ก็จะนำมาแสดงผลให้เราเห็นเป็นการชั่วคราว
